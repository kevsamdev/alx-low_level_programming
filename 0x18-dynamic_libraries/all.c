#include <stdio.h>
/**
 * add - add two integars
 * @a: first int
 * @b: the second int
 * Return: sum
*/
int add (int a, int b)
{
    return (a + b);
}
/**
 * sub - subtract two integars
 * @a: first int
 * @b: secomd int
 * Return: subtracted final result
*/
int sub (int a, int b)
{
    return (a - b);
}
/**
 * div - divide two integars
 * @a: first int
 * @b: second int
 * Return: division result
*/
int div(int a, int b)
{
    return (a / b);
}
/**
 * mod - mod of two integars
 * @a: first int
 * @b: second int
 * Return: final answer
*/
int mod (int a, int b)
{
    return (a % b);
}
/**
 * mul - multiply two integars
 * @a: first int
 * @b: second int
 * Return: final answer
*/
int mul (int a, int b)
{
    return (a * b);
}
